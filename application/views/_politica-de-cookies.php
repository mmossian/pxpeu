<h4>Cookies de sesión</h4>
<p>Las cookies de sesión son memorizadas y únicamente tienen validez temporal, de modo que no graban de forma permanente ninguna información en el disco duro de su ordenador. Estas cookies permiten almacenar la información relativa al inicio de sesión de cada usuario./<p>


<h4>Cookies de navegación</h4>
<p>El objetivo principal de las cookies de navegación es evitar ofrecerle recomendaciones no afines a sus intereses y ofrecerle propuestas comerciales dirigidas y personalizadas.<br>
<br>
